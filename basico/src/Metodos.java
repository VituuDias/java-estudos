public class Metodos {

    public static int somaDoisNumerosInteiros(int numero1, int numero2){
        return numero1 + numero2;


    }

    public static void main(String[] args) {
       //padrao de nomeação de metodos
       //verbo infinitivo, camelCase, sem ambiguidades
         //ex: calcularAreaRetangulo

        System.out.println(somaDoisNumerosInteiros(5, 10));

    }
}
